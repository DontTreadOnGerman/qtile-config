@hook.subscribe.startup
def autostart():
    subprocess.run(["sh", "/home/dtog/.config/qtile/autostart.sh"])


file = yaml.safe_load(open("/home/dtog/.config/qtile/config.yaml"))
mod = file["mod"]
terminal = file["terminal"]
browser = file["browser"]
